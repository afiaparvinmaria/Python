import math #python has a  build in module called math, which extends the list of the mathematical functions

#max function/min function
x=min(5,10,25)
y=max(5,10,25)
print(x)#5
print(y)#25

#absolute function
z= abs(-7.25)#returns absolute positive value
print(z)#7.25

#pow function
a=pow(2,3)#returns 8
print(a)

#math.sqrt() function
b=math.sqrt(25)#returns 5
print(b)


#math.ceil() function
#this method rounds a number upwards to its nearest
c=math.ceil(1.4)#upwards
print(c)

#math.floor() function
#this method rounds a number downwards to its nearest integer
d=math.floor(1.4)
print(d)


#pi
e=math.pi
print(e)

